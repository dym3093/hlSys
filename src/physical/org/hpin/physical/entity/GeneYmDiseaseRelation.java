package org.hpin.physical.entity;

import org.hpin.common.core.orm.BaseEntity;

public class GeneYmDiseaseRelation extends BaseEntity {

	private String id;
	
	private String geneCompanyCode;			//基因公司编码
	
	private String geneCompanyName;			//基因公司名字
	
	private String geneCompanyDiseaseCode;	//基因公司疾病编码
	
	private String geneCompanyDiseaseName;	//基因公司疾病名称
	
	private String ymDiseaseCode;			//远盟疾病编码
	
	private String ymDiseaseName;			//远盟疾病名称

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getGeneCompanyCode() {
		return geneCompanyCode;
	}

	public void setGeneCompanyCode(String geneCompanyCode) {
		this.geneCompanyCode = geneCompanyCode;
	}

	public String getGeneCompanyName() {
		return geneCompanyName;
	}

	public void setGeneCompanyName(String geneCompanyName) {
		this.geneCompanyName = geneCompanyName;
	}

	public String getGeneCompanyDiseaseCode() {
		return geneCompanyDiseaseCode;
	}

	public void setGeneCompanyDiseaseCode(String geneCompanyDiseaseCode) {
		this.geneCompanyDiseaseCode = geneCompanyDiseaseCode;
	}

	public String getGeneCompanyDiseaseName() {
		return geneCompanyDiseaseName;
	}

	public void setGeneCompanyDiseaseName(String geneCompanyDiseaseName) {
		this.geneCompanyDiseaseName = geneCompanyDiseaseName;
	}

	public String getYmDiseaseCode() {
		return ymDiseaseCode;
	}

	public void setYmDiseaseCode(String ymDiseaseCode) {
		this.ymDiseaseCode = ymDiseaseCode;
	}

	public String getYmDiseaseName() {
		return ymDiseaseName;
	}

	public void setYmDiseaseName(String ymDiseaseName) {
		this.ymDiseaseName = ymDiseaseName;
	}
	
}
