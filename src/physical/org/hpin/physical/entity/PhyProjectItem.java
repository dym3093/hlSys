package org.hpin.physical.entity;

import org.hpin.common.core.orm.BaseEntity;

public class PhyProjectItem extends BaseEntity {

	private String id;
	
	private String diseaseCode;		//疾病编码
	
	private String diseaseName;		//疾病名字
	
	private String sex;				//性别
	
	private Integer riskGrade;		//风险等级
	
	private String projectCode;		//体检项目编码
	
	private String projectName;		//体检项目名字

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getDiseaseCode() {
		return diseaseCode;
	}

	public void setDiseaseCode(String diseaseCode) {
		this.diseaseCode = diseaseCode;
	}

	public String getDiseaseName() {
		return diseaseName;
	}

	public void setDiseaseName(String diseaseName) {
		this.diseaseName = diseaseName;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}
	
	public Integer getRiskGrade() {
		return riskGrade;
	}

	public void setRiskGrade(Integer riskGrade) {
		this.riskGrade = riskGrade;
	}

	public String getProjectCode() {
		return projectCode;
	}

	public void setProjectCode(String projectCode) {
		this.projectCode = projectCode;
	}

	public String getProjectName() {
		return projectName;
	}

	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}
	
}
