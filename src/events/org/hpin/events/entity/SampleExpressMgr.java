package org.hpin.events.entity;

import java.math.BigDecimal;
import java.util.Date;
import org.hpin.common.core.orm.BaseEntity;

/**
 * 样本快递费管理PO
 * @author ybc
 * @since 2016/12/13
 */
public class SampleExpressMgr extends BaseEntity{

	private static final long serialVersionUID = 9083835472929379578L;
	
	private String id; //主键ID
	
	private String expressNum;//快递单号
	
	private String expressCompanyId;//快递公司ID
	
	private Integer receiveSendType;//寄送类型
	
	private Date receiveSendDate;//收发件日期
	
	private Integer isbill;//发票有无
	
	private String weight;//重量
	
	private BigDecimal totalCost;//快递费用
	
	private Integer expressContent;//包裹内容
	
	private String createUserId;//创建者ID
	
	private String createUserName;//创建者姓名
	
	private Integer isdeleted;//是否删除
	
	private Date createDate;//创建时间

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getExpressNum() {
		return expressNum;
	}

	public void setExpressNum(String expressNum) {
		this.expressNum = expressNum;
	}

	public String getExpressCompanyId() {
		return expressCompanyId;
	}

	public void setExpressCompanyId(String expressCompanyId) {
		this.expressCompanyId = expressCompanyId;
	}

	public Integer getReceiveSendType() {
		return receiveSendType;
	}

	public void setReceiveSendType(Integer receiveSendType) {
		this.receiveSendType = receiveSendType;
	}

	public Date getReceiveSendDate() {
		return receiveSendDate;
	}

	public void setReceiveSendDate(Date receiveSendDate) {
		this.receiveSendDate = receiveSendDate;
	}

	public Integer getIsbill() {
		return isbill;
	}

	public void setIsbill(Integer isbill) {
		this.isbill = isbill;
	}

	public String getWeight() {
		return weight;
	}

	public void setWeight(String weight) {
		this.weight = weight;
	}

	public BigDecimal getTotalCost() {
		return totalCost;
	}

	public void setTotalCost(BigDecimal totalCost) {
		this.totalCost = totalCost;
	}

	public Integer getExpressContent() {
		return expressContent;
	}

	public void setExpressContent(Integer expressContent) {
		this.expressContent = expressContent;
	}

	public String getCreateUserId() {
		return createUserId;
	}

	public void setCreateUserId(String createUserId) {
		this.createUserId = createUserId;
	}

	public String getCreateUserName() {
		return createUserName;
	}

	public void setCreateUserName(String createUserName) {
		this.createUserName = createUserName;
	}

	public Integer getIsdeleted() {
		return isdeleted;
	}

	public void setIsdeleted(Integer isdeleted) {
		this.isdeleted = isdeleted;
	}

	public Date getCreateDate() {
		return createDate;
	}

	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}

}
