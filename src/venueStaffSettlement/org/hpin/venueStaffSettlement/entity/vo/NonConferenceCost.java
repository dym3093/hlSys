package org.hpin.venueStaffSettlement.entity.vo;

import java.math.BigDecimal;
import java.util.Date;

public class NonConferenceCost {
	
	private String projectCode;		//项目编码
	private String projectUser;		//项目负责人
	private String projectName;		//项目名称
	private String projectType;		//项目类型
	private Date startTime;			//非会场费用的开始时间
	private Date endTime;			//非会场费用的结束时间
	private String month;			//月份
	private String OASerial;		//OA流水号
	
	private String name;				//姓名
	private BigDecimal travelCost;		//差旅费
	private BigDecimal cityCost;		//市内交通费
	private BigDecimal provinceCost;	//往返交通费
	private BigDecimal hotelCost;		//住宿费
	private BigDecimal laborCost;		//劳务费
	private BigDecimal otherCost;		//其他费用
	private BigDecimal amount;			//小计
	public String getProjectCode() {
		return projectCode;
	}
	public void setProjectCode(String projectCode) {
		this.projectCode = projectCode;
	}
	public String getProjectUser() {
		return projectUser;
	}
	public void setProjectUser(String projectUser) {
		this.projectUser = projectUser;
	}
	public String getProjectName() {
		return projectName;
	}
	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}
	public String getProjectType() {
		return projectType;
	}
	public void setProjectType(String projectType) {
		this.projectType = projectType;
	}
	public Date getStartTime() {
		return startTime;
	}
	public void setStartTime(Date startTime) {
		this.startTime = startTime;
	}
	public Date getEndTime() {
		return endTime;
	}
	public void setEndTime(Date endTime) {
		this.endTime = endTime;
	}
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public BigDecimal getTravelCost() {
		return travelCost;
	}
	public void setTravelCost(BigDecimal travelCost) {
		this.travelCost = travelCost;
	}
	public BigDecimal getCityCost() {
		return cityCost;
	}
	public void setCityCost(BigDecimal cityCost) {
		this.cityCost = cityCost;
	}
	public BigDecimal getProvinceCost() {
		return provinceCost;
	}
	public void setProvinceCost(BigDecimal provinceCost) {
		this.provinceCost = provinceCost;
	}
	public BigDecimal getHotelCost() {
		return hotelCost;
	}
	public void setHotelCost(BigDecimal hotelCost) {
		this.hotelCost = hotelCost;
	}
	public BigDecimal getLaborCost() {
		return laborCost;
	}
	public void setLaborCost(BigDecimal laborCost) {
		this.laborCost = laborCost;
	}
	public BigDecimal getOtherCost() {
		return otherCost;
	}
	public void setOtherCost(BigDecimal otherCost) {
		this.otherCost = otherCost;
	}
	public BigDecimal getAmount() {
		return amount;
	}
	public void setAmount(BigDecimal amount) {
		this.amount = amount;
	}
	public String getOASerial() {
		return OASerial;
	}
	public void setOASerial(String oASerial) {
		OASerial = oASerial;
	}
	
}
