package org.hpin.venueStaffSettlement.entity;

import java.io.Serializable;
import java.math.BigDecimal;
import java.util.Date;

import org.hpin.common.core.orm.BaseEntity;

/**
 * @author Carly
 * @since 2016年9月12日21:26:01
 * 场次管理导入异常数据service
 */
public class ErpConferenceCostException extends BaseEntity implements Serializable{
	
	private String id ;
	private String conferenceNo ;			//会议号
	private String name ;					//姓名
	private BigDecimal travelCost ;			//差旅费
	private BigDecimal instructorCost ;		//讲师费
	private BigDecimal cityTrafficCost ;	//室内交通费
	private BigDecimal provinceTrafficCost ;//交通往返费
	private BigDecimal hotelCost ;			//住宿费
	private BigDecimal sampleCost ;			//采样费
	private BigDecimal otherCost ;			//其他费用
	private BigDecimal amount ;				//小计
	private String descripe;	//异常描述 0：没有该场次信息，1：已有该用户
	private Date createTime ;
	private String createUser ;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getConferenceNo() {
		return conferenceNo;
	}
	public void setConferenceNo(String conferenceNo) {
		this.conferenceNo = conferenceNo;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public BigDecimal getTravelCost() {
		return travelCost;
	}
	public void setTravelCost(BigDecimal travelCost) {
		this.travelCost = travelCost;
	}
	public BigDecimal getInstructorCost() {
		return instructorCost;
	}
	public void setInstructorCost(BigDecimal instructorCost) {
		this.instructorCost = instructorCost;
	}
	public BigDecimal getCityTrafficCost() {
		return cityTrafficCost;
	}
	public void setCityTrafficCost(BigDecimal cityTrafficCost) {
		this.cityTrafficCost = cityTrafficCost;
	}
	public BigDecimal getProvinceTrafficCost() {
		return provinceTrafficCost;
	}
	public void setProvinceTrafficCost(BigDecimal provinceTrafficCost) {
		this.provinceTrafficCost = provinceTrafficCost;
	}
	public BigDecimal getHotelCost() {
		return hotelCost;
	}
	public void setHotelCost(BigDecimal hotelCost) {
		this.hotelCost = hotelCost;
	}
	public BigDecimal getSampleCost() {
		return sampleCost;
	}
	public void setSampleCost(BigDecimal sampleCost) {
		this.sampleCost = sampleCost;
	}
	public BigDecimal getOtherCost() {
		return otherCost;
	}
	public void setOtherCost(BigDecimal otherCost) {
		this.otherCost = otherCost;
	}
	public BigDecimal getAmount() {
		return amount;
	}
	public void setAmount(BigDecimal amount) {
		this.amount = amount;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	public String getCreateUser() {
		return createUser;
	}
	public void setCreateUser(String createUser) {
		this.createUser = createUser;
	}
	public String getDescripe() {
		return descripe;
	}
	public void setDescripe(String descripe) {
		this.descripe = descripe;
	}
	@Override
	public String toString() {
		return "ErpConferenceCostException [id=" + id + ", conferenceNo="
				+ conferenceNo + ", name=" + name + ", travelCost="
				+ travelCost + ", instructorCost=" + instructorCost
				+ ", cityTrafficCost=" + cityTrafficCost
				+ ", provinceTrafficCost=" + provinceTrafficCost
				+ ", hotelCost=" + hotelCost + ", sampleCost=" + sampleCost
				+ ", otherCost=" + otherCost + ", amount=" + amount
				+ ", descripe=" + descripe + ", createTime=" + createTime
				+ ", createUser=" + createUser + "]";
	}
	
	
}
